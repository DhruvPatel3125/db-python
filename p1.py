def addnumber(n1,n2):
    return(n1+n2)
def subnum(n1,n2):
    return(n1-n2)
def mulnum(n1,n2):
    return(n1*n2)
def div(n1,n2):
    return(n1/n2)
def max(n1,n2):
    if(n1>n2):
        return n1
    else:
        return n2
def min(n1,n2):
    if(n1<n2):
        return n2
    else:
        return n1
def per(n1,n2):
    return(n1+n2/100)

